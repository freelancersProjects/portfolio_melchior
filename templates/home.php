                <h2 class="text-center font-family-della-respira mb-5">Biographie</h2>
                <div class="col-md-7">
                    <p class="font-family-montserrat font-weight-semi-bold color-dark-blue fs-15px">
                        Pianiste depuis son jeune âge, Melchior REYNAUD suit une formation classique et intègre au lycée la classe <strong>TMD</strong> (Technique de la Musique et de la Danse) à Annecy.
                        Après avoir obtenu son bac à lauréat, Melchior est diplômé d’un <strong>DEM</strong> au conservatoire d’Annecy.<br><br>

                        Intéressé par la composition et notamment celle pour l’image, il intègre en 2022 le département
                        musique à l’image du CRD de Valence avec comme enseignant <strong>Nathanaël Bergèse.</strong><br><br>

                        Dès lors, il interprète et remet en musique des films muets restaurés avec la collaboration de la
                        <strong>Scène National LUX.</strong><br><br>

                        Il participe également au <strong>Festival d’Un Jour</strong> où il interprète et compose de la musique originale
                        pour des courts métrages animés.<br><br>

                        Melchior s’adapte et compose dans des styles variés pour répondre de manière cohérente à la
                        problématique d’un projet. Cependant, il développe sa patte de compositeur via la musique
                        électronique et tend vers les styles <strong>Ambient</strong> ou encore <strong>Hybride</strong>.
                        Il produit également de manière indépendante de la musique et compte deux albums à son bord.<br><br>

                        Pour cela, il puise dans son imaginaire puissant et créatif. Il s’aide de ses références ainsi que de sa culture musicale afin d’expérimenter et d’innover son travail.<br><br>
                        Récemment, il compose sa première bande originale pour la sortie du projet : <strong>LUSLHO -
                        L’Univers sans l’Homme.</strong> Cette musique accompagne la lecture d’un fanzine réalisé par <strong>Hugo de Faucompret</strong> et <strong>Camille Chao.</strong>
